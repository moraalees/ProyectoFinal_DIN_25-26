package com.example.gymtracker.model.enum_classes

/**
 * Enum que representa los diferentes enfoques de entrenamiento.
 *
 * Valores posibles:
 * - [HIPERTROFIA]: Enfoque en aumentar masa muscular.
 * - [DEFINICION]: Enfoque en definir y tonificar músculos.
 * - [PERDER_PESO]: Enfoque en pérdida de peso y reducción de grasa corporal.
 */
enum class Enfoque {
    HIPERTROFIA,
    DEFINICION,
    PERDER_PESO
}